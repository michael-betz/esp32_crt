// 40 MHz output without gaps
// outputs 2 bytes with CS low, then 2 bytes with CS high
// invert one of the CS lines so that this can drive 2x MCP4822T DACs

#include <stdint.h>
#include <stdlib.h>
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"

#include "i2s.h"
#include "draw.h"
#include "wifi.h"

#define SAMPLES_PER_FRAME 625000 / 100

static uint8_t dl_test[] = {
  0x02, 0x01, 0x06, 0x00, 0x00, 0x80, 0x00, 0x80, 0x18, 0xfc, 0x18, 0xfc,
  0x18, 0xfc, 0xe8, 0x03, 0xe8, 0x03, 0xe8, 0x03, 0xe8, 0x03, 0x18, 0xfc,
  0x18, 0xfc, 0x18, 0xfc, 0x03, 0x32, 0x00, 0x00, 0x00, 0x00, 0xbc, 0x02,
  0x00, 0x00, 0xd0, 0xdc, 0x03, 0x32, 0x00, 0x00, 0x00, 0x00, 0x20, 0x03,
  0x00, 0x00, 0xd0, 0xdc, 0x03, 0x32, 0x00, 0x00, 0x00, 0x00, 0x84, 0x03,
  0x00, 0x00, 0xd0, 0xdc, 0x05, 0x32, 0x2c, 0x01, 0x00, 0x00, 0xf9, 0xfc,
  0x0d, 0x11, 0x3c, 0x2d, 0x20, 0x2d, 0x3e, 0x0a, 0x48, 0x65, 0x6c, 0x6c,
  0x6f, 0x20, 0x57, 0x6f, 0x72, 0x6c, 0x64, 0xfe
};

static void i2s_stream_task(void *args)
{
	while (1) {
		push_list(dl_test, sizeof(dl_test));
	}
	vTaskDelete(NULL);
}

void app_main(void)
{
	printf("Hello, this is an SPI - test, I2S version!\n");

	initWifi();
	tryConnect();

	i2s_init();
	xTaskCreate(i2s_stream_task, "i2s_stream_task", 4096, NULL, 5, NULL);
}

